package org.example.rentoza.car;

/**
 * Car features enumeration - Safety, connectivity, and comfort features
 */
public enum Feature {
    // Safety Features
    ABS,                    // Anti-lock Braking System
    AIRBAG,                 // Airbags
    PARKING_SENSORS,        // Parking sensors
    REVERSE_CAMERA,         // Reverse camera
    BLIND_SPOT_MONITOR,     // Blind spot monitoring
    LANE_ASSIST,            // Lane keeping assist
    CRUISE_CONTROL,         // Cruise control
    ADAPTIVE_CRUISE,        // Adaptive cruise control

    // Connectivity Features
    BLUETOOTH,              // Bluetooth connectivity
    USB,                    // USB ports
    ANDROID_AUTO,           // Android Auto
    APPLE_CARPLAY,          // Apple CarPlay
    NAVIGATION,             // GPS Navigation
    WIFI,                   // WiFi hotspot

    // Comfort Features
    AIR_CONDITIONING,       // Air conditioning
    CLIMATE_CONTROL,        // Dual-zone climate control
    HEATED_SEATS,           // Heated seats
    LEATHER_SEATS,          // Leather seats
    SUNROOF,                // Sunroof
    PANORAMIC_ROOF,         // Panoramic sunroof
    KEYLESS_ENTRY,          // Keyless entry
    PUSH_START,             // Push button start
    ELECTRIC_WINDOWS,       // Electric windows
    POWER_STEERING,         // Power steering

    // Additional Features
    ROOF_RACK,              // Roof rack
    TOW_HITCH,              // Tow hitch
    ALLOY_WHEELS,           // Alloy wheels
    LED_LIGHTS,             // LED headlights
    FOG_LIGHTS              // Fog lights
}
